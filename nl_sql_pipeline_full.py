def build_full_schema(db_id: str) -> str:
    """Full schema -- all tables, used by both DBs' 'full' strategy."""
    return ALL_DB_DATA[db_id]["db"].get_table_info()


def build_bm25_schema(db_id: str, question: str, top_k: int = 5) -> str:
    """BM25-only retrieval, formula_1 only (debit_card never calls this)."""
    data = ALL_DB_DATA[db_id]
    tokenized_query = tokenize(question)
    scores = data["bm25"].get_scores(tokenized_query)
    ranked = sorted(zip(data["table_names"], scores), key=lambda x: -x[1])
    top_tables = [t for t, score in ranked[:top_k]]
    return "\n\n".join(data["table_chunks"][t] for t in top_tables)


def build_embedding_schema(db_id: str, question: str, top_k: int = 5) -> str:
    """Embedding-only retrieval, formula_1 only."""
    data = ALL_DB_DATA[db_id]
    query_embedding = vo.embed([question], model="voyage-4", input_type="query").embeddings[0]
    scores = [cosine_similarity(query_embedding, emb) for emb in data["table_embeddings"]]
    ranked = sorted(zip(data["table_names"], scores), key=lambda x: -x[1])
    top_tables = [t for t, score in ranked[:top_k]]
    return "\n\n".join(data["table_chunks"][t] for t in top_tables)


def rrf_fuse(bm25_ranked: list, embedding_ranked: list, table_names: list, k: int = 60) -> list:
    """Reciprocal Rank Fusion -- identical logic to the notebook, with
    table_names passed explicitly instead of closing over a global."""
    bm25_ranks = {table: rank + 1 for rank, (table, _) in enumerate(bm25_ranked)}
    embedding_ranks = {table: rank + 1 for rank, (table, _) in enumerate(embedding_ranked)}

    rrf_scores = {}
    for table in table_names:
        rrf_scores[table] = 1 / (k + bm25_ranks[table]) + 1 / (k + embedding_ranks[table])

    return sorted(rrf_scores.items(), key=lambda x: -x[1])


def build_retrieved_schema(db_id: str, question: str, top_k: int = 5) -> str:
    """Hybrid retrieval via RRF fusion -- formula_1 only."""
    data = ALL_DB_DATA[db_id]
    table_names = data["table_names"]

    tokenized_query = tokenize(question)
    bm25_scores = data["bm25"].get_scores(tokenized_query)
    bm25_ranked = sorted(zip(table_names, bm25_scores), key=lambda x: -x[1])

    query_embedding = vo.embed([question], model="voyage-4", input_type="query").embeddings[0]
    embedding_scores = [cosine_similarity(query_embedding, emb) for emb in data["table_embeddings"]]
    embedding_ranked = sorted(zip(table_names, embedding_scores), key=lambda x: -x[1])

    fused = rrf_fuse(bm25_ranked, embedding_ranked, table_names, k=60)
    top_tables = [t for t, score in fused[:top_k]]
    return "\n\n".join(data["table_chunks"][t] for t in top_tables)


