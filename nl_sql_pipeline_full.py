"""There are three moving parts worth understanding:

1. We fetch the `.sql` script, not a `.sqlite` binary. The script is just plain text — `CREATE TABLE` and `INSERT` statements. `requests.get(url).text` grabs it, and `connection.executescript(sql_script)` runs the whole thing against a fresh database in one shot. Text scripts from a real repo are far less likely to vanish than a one-off binary asset sitting in a storage bucket.
2. `sqlite3.connect(":memory:")` creates the database entirely in RAM — there's no `Chinook.db` file at all this time. Good for Colab: nothing to save to disk, nothing that can end up partially downloaded.
3. The `StaticPool` part is the subtle bit. Normally `SQLAlchemy` opens a new raw connection every time you run a query. That's fine for a file-based db, but an in-memory SQLite db is tied to one specific connection — close it, and the data is gone. `creator=lambda: connection` tells SQLAlchemy *"always hand back this exact connection object I already built,"* and `StaticPool` stops it from ever closing/replacing that connection. Without this, you'd get an empty database on your second query.
"""

import sqlite3
import requests
from sqlalchemy import create_engine
from sqlalchemy.pool import StaticPool
from langchain_community.utilities import SQLDatabase

def get_engine_for_chinook_db():
    """Pull the Chinook SQL script and build an in-memory SQLite database from it."""
    url = "https://raw.githubusercontent.com/lerocha/chinook-database/master/ChinookDatabase/DataSources/Chinook_Sqlite.sql"
    sql_script = requests.get(url).text

    connection = sqlite3.connect(":memory:", check_same_thread=False)
    connection.executescript(sql_script)
    return create_engine(
        "sqlite://",
        creator=lambda: connection,
        poolclass=StaticPool,
        connect_args={"check_same_thread": False},
    )

