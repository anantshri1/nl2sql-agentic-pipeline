# schema-builder functions
# Two things worth flagging:
# 1. The notebook versions took (question, top_k=5) since they only ever operated on formula1_db. These take (db_id, question, top_k=5) — db_id first — because the app needs to route to the right DB's data.
# 2. build_bm25_schema and build_embedding_schema will only ever be called with db_id="formula_1" in practice (since debit_card's strategy dropdown is hidden), but I didn't hardcode that restriction into the functions themselves — they'd technically crash with a KeyError if called on debit_card_specializing (no "bm25" key in its data dict).
