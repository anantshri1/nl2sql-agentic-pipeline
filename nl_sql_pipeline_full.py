def on_db_change(db_id):
    """When the DB changes: show/hide retrieval controls, update the
    curated quick-fill buttons, update the description text."""
    supports_retrieval = DB_CONFIG[db_id]["supports_retrieval"]
    description = DB_CONFIG[db_id]["description"]
    questions = get_curated_questions(db_id)

    # Pad to exactly 3 buttons' worth of updates regardless of how many
    # curated examples exist, so the UI doesn't break if that count changes
    btn_updates = []
    for i in range(3):
        if i < len(questions):
            btn_updates.append(gr.update(value=questions[i], visible=True))
        else:
            btn_updates.append(gr.update(visible=False))

    return (
        gr.update(visible=supports_retrieval),   # strategy dropdown
        gr.update(visible=supports_retrieval),   # k slider
        description,
        *btn_updates,
    )


