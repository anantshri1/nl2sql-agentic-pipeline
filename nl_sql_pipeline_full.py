def categorize_failure(result: dict, db: SQLDatabase):
    """
    Lightweight auto-tagging for failed eval examples. Only two categories
    are cheaply and reliably detectable from execution output alone:
    floating-point near-miss and empty predicted result. Everything else
    is semantically subtle (wrong table, ambiguous aggregation unit, gold
    dataset defects) and genuinely requires a human reading the SQL — so
    it's labeled "needs_manual_review" rather than guessed at.
    """
    predicted_sql = result["predicted_sql"]
    gold_sql = result["gold_sql"]

    if predicted_sql is None:
        return "predicted_sql_generation_failed"

    try:
        predicted_result = execute_for_grading(db, predicted_sql)
    except Exception:
        return "predicted_sql_execution_error"

    gold_result = execute_for_grading(db, gold_sql)

    if predicted_result == []:
        return "empty_predicted_result"

    # Floating-point near-miss: same shape, all-numeric, tiny relative diff
    if len(predicted_result) == len(gold_result):
        try:
            all_close = all(
                len(p_row) == len(g_row) and all(
                    abs(p - g) <= 1e-6 * max(abs(g), 1e-9)
                    for p, g in zip(p_row, g_row)
                )
                for p_row, g_row in zip(predicted_result, gold_result)
            )
            if all_close:
                return "floating_point_near_miss"
        except TypeError:
            pass  # non-numeric cells, not a float comparison case

    return "needs_manual_review"

