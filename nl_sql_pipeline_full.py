def get_retrieved_tables(db_id: str, strategy: str, schema_text: str) -> list[str]:
    """Same substring-detection trick as Block 3's precomputation: a table
    'made it into' the schema if its chunk text appears verbatim in the
    concatenated schema string. Exact for 'full' (all tables, trivially)."""
    data = ALL_DB_DATA[db_id]
    if strategy == "full":
        return sorted(data["table_chunks"].keys())
    table_names = data.get("table_names", list(data["table_chunks"].keys()))
    return sorted(t for t in table_names if data["table_chunks"][t] in schema_text)


def build_schema_for_strategy(db_id: str, strategy: str, question: str, k: int) -> str:
    """Routes to the correct schema-builder. debit_card_specializing only
    ever uses 'full' -- this is enforced here, not just by the UI hiding
    the dropdown, so a bad db_id/strategy combo fails loudly instead of
    silently calling the wrong function."""
    if not DB_CONFIG[db_id]["supports_retrieval"] and strategy != "full":
        raise ValueError(f"{db_id} does not support retrieval strategy '{strategy}'")

    if strategy == "full":
        return build_full_schema(db_id)
    elif strategy == "bm25":
        return build_bm25_schema(db_id, question, top_k=k)
    elif strategy == "embedding":
        return build_embedding_schema(db_id, question, top_k=k)
    elif strategy == "hybrid":
        return build_retrieved_schema(db_id, question, top_k=k)
    else:
        raise ValueError(f"Unknown strategy: {strategy}")


def format_result(rows: list) -> str:
    """Plain-text formatting for the result textbox -- one row per line,
    truncated if huge so the UI doesn't choke on a 10,000-row SELECT *."""
    if not rows:
        return "(empty result)"
    display_rows = rows[:20]
    lines = [str(r) for r in display_rows]
    if len(rows) > 20:
        lines.append(f"... ({len(rows) - 20} more rows)")
    return "\n".join(lines)


def find_curated_match(db_id: str, question: str):
    """Exact string match against this DB's curated questions. Returns
    the matching precomputed record dict, or None if this is a free-text
    question with no curated counterpart."""
    examples = ALL_DB_DATA[db_id].get("curated_examples", [])
    question_stripped = question.strip()
    for ex in examples:
        if ex["question"].strip() == question_stripped:
            return ex
    return None


def format_gold_comparison(record: dict, strategy: str) -> str:
    """Builds the markdown block showing gold SQL/result alongside the
    correctness verdict for the matched strategy. Falls back gracefully
    if this curated record doesn't have data for the requested strategy
    (shouldn't happen given our k=5-always lookup, but defensive anyway)."""
    strategy_data = record["strategies"].get(strategy)
    if strategy_data is None:
        return "_(no precomputed result for this strategy on this curated question)_"

    verdict = "✅ Correct" if strategy_data["correct"] else "❌ Incorrect"
    gold_result_str = format_result(record["gold_result"])

    return (
        f"**Curated question — verdict: {verdict}**\n\n"
        f"**Gold SQL:**\n```sql\n{record['gold_sql']}\n```\n\n"
        f"**Gold result:**\n```\n{gold_result_str}\n```"
    )


def run_query(db_id: str, strategy: str, k: int, question: str):
    """Real implementation. Checks for a curated match first (instant,
    free, always correct) before falling through to live generation."""
    if not question or not question.strip():
        return "_(enter a question first)_", "", "", "", ""

    curated_match = find_curated_match(db_id, question)
    if curated_match is not None:
        strategy_data = curated_match["strategies"].get(strategy)
        if strategy_data is not None:
            retrieved_tables_str = ", ".join(strategy_data["retrieved_tables"])
            sql = strategy_data["predicted_sql"]
            result_text = format_result(strategy_data["predicted_result"])
            gold_block = format_gold_comparison(curated_match, strategy)
            return retrieved_tables_str, sql, result_text, gold_block, ""
        # Curated question, but this strategy wasn't precomputed for it
        # (only possible for debit_card_specializing's non-"full" case,
        # which the UI shouldn't allow -- but fail safely if it happens)
        return ("_(precomputed result unavailable for this strategy)_", "", "", "", "")

    # ---- Free-text path: everything from Block 5c, unchanged ----
    db = ALL_DB_DATA[db_id]["db"]

    try:
        schema_text = build_schema_for_strategy(db_id, strategy, question, k)
        retrieved_tables = get_retrieved_tables(db_id, strategy, schema_text)
    except Exception as e:
        return f"Error building schema: {e}", "", "", "", ""

    try:
        predicted_sql = ask_db_with_retry(question, db, evidence="", schema=schema_text)
    except RuntimeError as e:
        return ", ".join(retrieved_tables), "", f"SQL generation failed: {e}", "", ""
    except Exception as e:
        return ", ".join(retrieved_tables), "", f"Unexpected error during generation: {e}", "", ""

    try:
        result_rows = execute_for_grading(db, predicted_sql)
        result_text = format_result(result_rows)
    except Exception as e:
        result_text = f"Query failed to execute: {e}"

    return ", ".join(retrieved_tables), predicted_sql, result_text, "", ""

