"""We now have all three pieces the `eval` loop needs:
* `ask_db_with_retry(question, db, evidence, max_retries)` → generates a prediction
* `execute_for_grading(db, sql)` → structured results
* `is_correct(predicted_sql, gold_sql, db)` → scores one example

A few design decisions worth pinning down:
* What does `ask_db_with_retry` return on success? Right now it returns `db.run(sql_query)` — the string result, not the SQL itself. But `is_correct` needs the SQL string to re-execute it via `execute_for_grading`, not the already-stringified result. So we have a mismatch: the eval loop needs the query text, not what `ask_db_with_retry` currently hands back.
* What happens on total failure (all `max_retries` exhausted)? Currently it raises `RuntimeError`. In an eval loop over 30 examples, one bad example shouldn't kill the whole run.

Both of these point to the same fix: `ask_db_with_retry` should return the SQL query string (not the executed result), and the eval loop calls `execute_for_grading` itself. That also matches the existing pattern — `execute_for_grading` is the single place where "run query → structured result" happens, so we shouldn't duplicate that logic inside `ask_db_with_retry`.

`ask_db_with_retry` now returns SQL text on success and raises on total failure.

Now the actual eval loop. Here's the plan for this block:

* Loop over `debit_card_examples`
* For each: call `ask_db_with_retry`, catch the `RuntimeError` if it gives up (score as incorrect, don't crash the run)
* Score each with `is_correct`
* Track per-example results (question_id, correct/incorrect, predicted SQL) so you can inspect failures afterward — not just a final percentage
* Print a running tally and final accuracy

A couple of design notes:

* Why catch `RuntimeError` here and not inside `ask_db_with_retry`: `ask_db_with_retry` stays loud (`raises`), the eval loop is the place that knows "one bad example shouldn't kill 29 others."
* Why store full per-example dicts instead of just a running correct/incorrect count: when accuracy comes back at, say, 70%, you'll want to look at which 9 examples failed and why (bad SQL? gold SQL using a join your prompt didn't anticipate? evidence not parsed correctly?) — that's the actual debugging value of an eval harness vs. just a number.
>One real cost to flag: this will make `len(debit_card_examples) × (1 to 4)` LLM calls — for 30 examples, worst case ~120 calls if every single one needs all 3 retries. At `temp=0` on a clean small-ish DB, most should succeed on attempt 1, so in practice it'll likely be much closer to 30-40 calls. Still, this is the most expensive block we've run so far.
"""

def run_eval(examples: list, db: SQLDatabase, max_retries: int = 3):
    """
    Run the full predict-and-grade loop over a list of BIRD examples.
    Returns a list of per-example result dicts for later inspection.
    """
    results = []

    for i, ex in enumerate(examples):
        question = ex["question"]
        evidence = ex.get("evidence", "")
        gold_sql = ex["SQL"]

        try:
            predicted_sql = ask_db_with_retry(question, db, evidence=evidence, max_retries=max_retries)
            correct = is_correct(predicted_sql, gold_sql, db)
        except RuntimeError as e:
            print(f"  Gave up generating SQL: {str(e)[:200]}")
            predicted_sql = None
            correct = False

        results.append({
            "question_id": ex["question_id"],
            "question": question,
            "predicted_sql": predicted_sql,
            "gold_sql": gold_sql,
            "correct": correct,
        })

        status = "✓" if correct else "✗"
        print(f"[{i+1}/{len(examples)}] {status}  {question[:80]}")

    accuracy = sum(r["correct"] for r in results) / len(results)
    print(f"\nAccuracy: {accuracy:.1%} ({sum(r['correct'] for r in results)}/{len(results)})")
    return results

