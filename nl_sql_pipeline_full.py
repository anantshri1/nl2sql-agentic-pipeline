def load_db_data(db_id: str) -> dict:
    """Load table_chunks, tokenized_corpus, table_embeddings, precomputed
    curated examples, and build a live SQLDatabase connection + (if this
    DB supports retrieval) a rebuilt BM25Okapi index -- all once, at
    startup, mirroring the notebook's "build once, reuse" caching pattern."""
    base = os.path.join(DATA_DIR, db_id)
    data = {}
    try:
        with open(os.path.join(base, "table_chunks.json")) as f:
            data["table_chunks"] = json.load(f)
        with open(os.path.join(base, "examples_precomputed.json")) as f:
            data["curated_examples"] = json.load(f)

        sqlite_path = os.path.join(base, f"{db_id}.sqlite")
        data["db"] = SQLDatabase.from_uri(f"sqlite:///{sqlite_path}")

        if DB_CONFIG[db_id]["supports_retrieval"]:
            with open(os.path.join(base, "tokenized_corpus.json")) as f:
                data["tokenized_corpus"] = json.load(f)
            with open(os.path.join(base, "table_embeddings.json")) as f:
                data["table_embeddings"] = json.load(f)
            with open(os.path.join(base, "table_names.json")) as f:
                data["table_names"] = json.load(f)
            # Rebuild BM25Okapi in-memory from the saved tokenized corpus --
            # pure Python, no network call, milliseconds even for 13 tables
            data["bm25"] = BM25Okapi(data["tokenized_corpus"])
    except FileNotFoundError as e:
        print(f"[load_db_data] Warning: missing data file for {db_id}: {e}")
        data.setdefault("curated_examples", [])
        data.setdefault("table_chunks", {})
    return data


