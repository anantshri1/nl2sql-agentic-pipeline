def is_correct(predicted_sql: str, gold_sql: str, db: SQLDatabase) -> bool:
    """
    Execution-accuracy check: run both queries, compare results as
    ordered lists of tuples. Either side failing to execute (including
    gold SQL hitting a SQLAlchemy parameter-binding quirk on literal
    '?' characters) is scored as incorrect, not raised -- a single
    malformed query must never crash a full eval run.
    """
    try:
        gold_result = execute_for_grading(db, gold_sql)
    except Exception as e:
        print(f"  Gold SQL failed to execute: {str(e)[:200]}")
        return False

    try:
        predicted_result = execute_for_grading(db, predicted_sql)
    except Exception as e:
        print(f"  Predicted SQL failed to execute: {str(e)[:200]}")
        return False

    return predicted_result == gold_result

