def run_eval(examples: list, db: SQLDatabase, max_retries: int = 3, schema_fn=None,
             delay_seconds: float = 1.0, checkpoint_path: str = None):
    """
    Same as your patched version, with checkpointing: after every
    example, results-so-far are written to checkpoint_path (if given).
    If the run crashes or disconnects partway through, the checkpoint
    file on disk still has everything completed up to that point --
    nothing is lost except whatever Voyage/Claude calls happen after
    the last successful write.
    """
    results = []

    for i, ex in enumerate(examples):
        question = ex["question"]
        evidence = ex.get("evidence", "")
        gold_sql = ex["SQL"]

        schema = schema_fn(question) if schema_fn is not None else None

        try:
            predicted_sql = ask_db_with_retry(question, db, evidence=evidence, max_retries=max_retries, schema=schema)
            correct = is_correct(predicted_sql, gold_sql, db)
        except RuntimeError as e:
            print(f"  Gave up generating SQL: {str(e)[:200]}")
            predicted_sql = None
            correct = False

        results.append({
            "question_id": ex["question_id"],
            "question": question,
            "predicted_sql": predicted_sql,
            "gold_sql": gold_sql,
            "correct": correct,
        })

        status = "✓" if correct else "✗"
        print(f"[{i+1}/{len(examples)}] {status}  {question[:80]}")

        if checkpoint_path:
            with open(checkpoint_path, "w") as f:
                json.dump(make_serializable_eval(results), f, indent=2)

        if i < len(examples) - 1:
            time.sleep(delay_seconds)

    accuracy = sum(r["correct"] for r in results) / len(results)
    print(f"\nAccuracy: {accuracy:.1%} ({sum(r['correct'] for r in results)}/{len(results)})")
    return results

