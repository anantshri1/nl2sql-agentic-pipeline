"""# **NL $\to$ SQL Chain**

Three concepts to flag here, since they're core `LangChain` idioms:

* `sql_prompt | llm` is LCEL (LangChain Expression Language) — the pipe operator chains components together into a `Runnable`. It's not Python's bitwise OR being abused; `ChatPromptTemplate` and `ChatAnthropic` both implement a common `Runnable` interface, so | composes them: output of the prompt (a formatted message) becomes the input to the LLM. This pattern — `prompt | model | parser` — is the backbone of basically everything in modern LangChain.
* `db.get_table_info()` is the payoff from Block 2 — it returns the full schema as a text description (table names, columns, types, even sample rows), which we inject into the prompt as `{schema}`. **This is literally how the LLM "knows" your database**.
* `clean_sql()` exists because LLMs are unreliable about following exact formatting instructions. Even though the prompt says `"no markdown,"` Claude (or any LLM) will sometimes still wrap output in ` ```sql` fences anyway.
> Never fully trust raw LLM output; always validate/clean it before using it.
"""

from langchain_core.prompts import ChatPromptTemplate

sql_prompt = ChatPromptTemplate.from_messages([
    ("system",
     "You are a SQL expert. Given an input question, write a syntactically correct "
     "{dialect} query to answer it.\n\n"
     "Only use the following tables:\n{schema}\n\n"
     "Return ONLY the SQL query. No explanation, no markdown formatting, no semicolon."),
    ("human", "{question}")
])

sql_generation_chain = sql_prompt | llm

def clean_sql(raw_text: str) -> str:
    """Strip markdown code fences in case the LLM adds them despite instructions."""
    text = raw_text.strip()
    if text.startswith("```"):
        text = text.strip("`")
        text = text.replace("sql\n", "", 1).replace("sql", "", 1)
    return text.strip()

def ask_db(question: str):
    schema = db.get_table_info()
    response = sql_generation_chain.invoke({
        "dialect": db.dialect,
        "schema": schema,
        "question": question
    })
    sql_query = clean_sql(response.content)
    print("Generated SQL:", sql_query)
    result = db.run(sql_query)
    return result

