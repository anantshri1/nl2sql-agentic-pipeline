"""> `_execute()` works as expected on this LangChain version.

Now we have both pieces needed for grading: `execute_for_grading()` for structured results, and `ask_db_with_retry()` (refactored) for generating predictions. Next block is where we actually wire these into a comparison function — something like `is_correct(predicted_sql, gold_sql, db)` that runs both through execute_for_grading and does the exact ordered tuple-list comparison we just agreed on.

> What happens when the predicted SQL fails to execute at all (even after ask_db_with_retry's 3 attempts) $\to$ In execution-accuracy grading, a query that errors out should obviously be scored as incorrect rather than crashing the whole loop. So `is_correct` needs to catch exceptions from `execute_for_grading(predicted_sql)` and just return `False` rather than propagate — gold SQL we can assume always succeeds (it's from BIRD's verified set), so no need to wrap that side defensively.
"""

def is_correct(predicted_sql: str, gold_sql: str, db: SQLDatabase) -> bool:
    """
    Execution-accuracy check: run both queries, compare results as
    ordered lists of tuples. Predicted SQL that fails to execute is
    simply scored as incorrect (not raised).
    """
    gold_result = execute_for_grading(db, gold_sql)

    try:
        predicted_result = execute_for_grading(db, predicted_sql)
    except Exception as e:
        print(f"  Predicted SQL failed to execute: {str(e)[:200]}")
        return False

    return predicted_result == gold_result

