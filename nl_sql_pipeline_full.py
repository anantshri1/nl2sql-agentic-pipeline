"""# **Eval Harness**

`db.run()` returns a stringified result like `"[(0.0657...,)]"` instead of something you can actually compare programmatically. That's the right thing to fix before writing the scoring loop — comparing strings of floats is going to give you false negatives from formatting alone (e.g. 0.0657 vs 0.06569999999999999).

The core design decision for this block:
Keep `db.run()` for the agent-facing path (since LLMs read strings fine) but add a separate helper used only for grading that returns real Python objects. Essentially, we don't touch the existing `ask_db_with_retry` path at all, just add a new small function like `execute_for_grading(db, sql)` that returns a list of tuples. This keeps your working retry loop untouched and gives the eval harness its own clean interface.


Why `db._execute()` instead of `db.run()`: `db.run()` internally calls `db._execute()` and then formats the result into a string for LLM consumption — that string formatting is exactly the step we want to skip. `db._execute()` returns a list of `RowMapping` objects (dict-like), so `row.values()` pulls out the actual typed Python values (floats stay floats, ints stay ints), and wrapping in `tuple()` gives us something directly comparable with `==`.

> One uncertainty to flag: `_execute()` is a leading-underscore "private" method on `SQLDatabase`. It works today, but it's not part of LangChain's stable public API, so a future langchain-community version could rename or change it without notice. If that ever breaks, the fallback is dropping straight to `SQLAlchemy` `(engine.connect().execute(text(sql)).fetchall())`, bypassing LangChain's wrapper entirely.
"""

def execute_for_grading(db: SQLDatabase, sql: str):
    """
    Run a SQL query and return raw, structured rows (list of tuples)
    instead of db.run()'s stringified output. Used only for grading,
    not for the agent-facing ask_db / ask_db_with_retry path.
    """
    result = db._execute(sql)
    return [tuple(row.values()) for row in result]

