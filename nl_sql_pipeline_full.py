def ask_db_with_retry(question: str, db, evidence: str = "", max_retries: int = 3, schema: str = None):
    if schema is None:
        schema = db.get_table_info()  # unchanged default behavior

    response = sql_generation_chain.invoke({
        "dialect": db.dialect, "schema": schema, "question": question, "evidence": evidence
    })
    sql_query = clean_sql(response.content)

    for attempt in range(max_retries):
        print(f"Attempt {attempt + 1}: {sql_query}")
        try:
            result = db.run(sql_query)
            return sql_query
        except Exception as e:
            error_message = str(e)
            print(f"  -> Failed: {error_message[:200]}")
            response = correction_chain.invoke({
                "dialect": db.dialect, "schema": schema, "question": question,
                "evidence": evidence, "bad_query": sql_query, "error": error_message
            })
            sql_query = clean_sql(response.content)

    raise RuntimeError(f"Gave up after {max_retries} attempts. Last query: {sql_query}")

