def _collect_cte_names(token_list) -> set[str]:
    """Find names declared via WITH name AS (...) [, name2 AS (...)]*
    so we can exclude them later -- they're query-local aliases, not
    real schema tables, even though they look identical to one once
    referenced later in a FROM clause."""
