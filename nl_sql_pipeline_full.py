"""## **Rank Reciprocal Fusion (RRF)**

This is the piece that turns two separate ranked lists (BM25's and the embedding's) into one combined ranking.

RRF doesn't combine raw scores (which would be apples-to-oranges — BM25 scores are unbounded term-frequency weights, cosine similarity is bounded `[-1, 1]`) — instead it combines ranks. For each table, you take its rank position in the BM25 list and its rank position in the embedding list, convert each rank to `1 / (k + rank)` (`k` is a small constant, commonly 60, that dampens the effect of rank-1 dominating too heavily), and sum those two reciprocal-rank scores. Whichever table has the highest combined reciprocal-rank score wins — a table that's mediocre-but-present in both lists can beat a table that's #1 in one list but absent from the other.

A couple of things worth noting about the implementation:
* `bm25_ranks` and `embedding_ranks` are dicts keyed by table name, not positional lists — this is deliberate, since BM25's ranking order and the embedding's ranking order will generally disagree with each other (that's the whole point of fusing two different signals), so we can't just zip two lists assuming aligned positions. Looking each table up by name in both dicts is what correctly handles that disagreement.
* for table in `table_names` (not for table in `bm25_ranked`) ensures we compute an RRF score for every one of the 13 tables exactly once, regardless of which method ranked it where.
* One thing to watch when you look at the output: since `pitStops` scored highly on both BM25 and embeddings for the first query, expect it to dominate the fused ranking even more decisively than either individual method showed — that's RRF correctly rewarding consensus between the two signals.

"""

def rrf_fuse(bm25_ranked: list, embedding_ranked: list, k: int = 60) -> list:
    """
    Reciprocal Rank Fusion. Takes two full rankings (table_name, score) tuples
    -- already sorted descending by relevance -- and combines them into one
    fused ranking based on rank position, not raw score magnitude.

    For each table: rrf_score = 1/(k + bm25_rank) + 1/(k + embedding_rank)
    Rank positions are 1-indexed (the top result has rank 1, not 0) --
    this matches the convention in the original RRF paper.
    """
    bm25_ranks = {table: rank + 1 for rank, (table, _) in enumerate(bm25_ranked)}
    embedding_ranks = {table: rank + 1 for rank, (table, _) in enumerate(embedding_ranked)}

    rrf_scores = {}
    for table in table_names:
        bm25_rank = bm25_ranks[table]
        embedding_rank = embedding_ranks[table]
        rrf_scores[table] = 1 / (k + bm25_rank) + 1 / (k + embedding_rank)

    return sorted(rrf_scores.items(), key=lambda x: -x[1])

def hybrid_retrieve(query: str, top_k: int = 5):
    """Full pipeline: rank all 13 tables by BM25, rank all 13 by embedding
    similarity, fuse via RRF, return the top_k fused result."""
    tokenized_query = tokenize(query)
    bm25_scores = bm25.get_scores(tokenized_query)
    bm25_ranked = sorted(zip(table_names, bm25_scores), key=lambda x: -x[1])

    query_embedding = vo.embed([query], model="voyage-4", input_type="query").embeddings[0]
    embedding_scores = [cosine_similarity(query_embedding, table_embeddings[i]) for i in range(len(table_names))]
    embedding_ranked = sorted(zip(table_names, embedding_scores), key=lambda x: -x[1])

    fused = rrf_fuse(bm25_ranked, embedding_ranked, k=60)
    return fused[:top_k]

