"""## **Building the Retrieval Quality Metric**

A few notes on what's happening:
* `overlap_fraction` defaults to `1.0` when `gold_tables` is empty — a defensive guard against division by zero, though we already confirmed all 66 examples have non-empty gold table sets, so this branch should never actually trigger here. Leaving it in anyway since it's cheap insurance when this function gets reused later on a different dataset.
* This calls `vo.embed()` once per example (inside `hybrid_retrieve`), so 66 examples means 66 Voyage API calls — still trivial against the 200M free-tier budget, but worth knowing this isn't instantaneous; expect it to take a noticeable few seconds to run, not be instant like the BM25-only checks were.
* One real risk worth flagging before we run this: if `hybrid_retrieve`'s `top_k` slices the fused list (which it does, per how we built it), and a gold table happens to tie with several others near the cutoff boundary, recall could be sensitive to exactly where `top_k` lands — which is precisely what the `k ∈ {3,5,7}` sweep is designed to characterize, so seeing some sensitivity here isn't a red flag, it's the expected signal.
"""

import time

def retrieval_quality(question: str, gold_sql: str, top_k: int = 5) -> dict:
    """
    For one example: run hybrid retrieval, compare against gold tables.
    Returns recall (did retrieved top_k contain ALL gold tables) and
    overlap fraction (what fraction of gold tables were found) --
    same distinction as agreed earlier: recall is the binary/strict
    view, overlap is the more granular one.
    """
    gold_tables = extract_tables_from_sql(gold_sql)
    retrieved = hybrid_retrieve(question, top_k=top_k)
    retrieved_tables = {t for t, score in retrieved}

    found = gold_tables & retrieved_tables
    recall = 1.0 if gold_tables.issubset(retrieved_tables) else 0.0
    overlap_fraction = len(found) / len(gold_tables) if gold_tables else 1.0

    return {
        "gold_tables": gold_tables,
        "retrieved_tables": retrieved_tables,
        "recall": recall,
        "overlap_fraction": overlap_fraction,
    }

import time

def retrieval_quality_batch(examples: list, top_k: int = 5, delay_seconds: float = 1.0) -> list:
    """
    Same as retrieval_quality, but runs across a list of examples with a
    delay between Voyage API calls to respect rate limits. delay_seconds
    defaults to 1.0 (60 RPM) -- bump to ~20s.
    """
    results = []
    for i, ex in enumerate(examples):
        result = retrieval_quality(ex["question"], ex["SQL"], top_k=top_k)
        result["question_id"] = ex["question_id"]
        result["question"] = ex["question"]
        results.append(result)
        print(f"[{i+1}/{len(examples)}] done")
        if i < len(examples) - 1:  # no need to sleep after the last one
            time.sleep(delay_seconds)
    return results

